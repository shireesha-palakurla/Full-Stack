//loops
 // for loop
 for(let i=1;i<=5;i++){
    console.log(i);
 }
 //while loop
 var i=1;
 while(i<=5){
    console.log(i);
    i++;
 }

 //do while loop
 var i=1;
 do{
    console.log(i);
    i++;
 }while(i<=5);

 //for of
 for(let i of [1,2,3,4])
 {
    console.log(i);
 }
 //for in
 var obj={name:"siri",dept:"it"};
 for(let i in obj){
     console.log(i,obj[i]);
 }
